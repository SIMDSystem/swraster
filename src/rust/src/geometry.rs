//! geometry.rs — vertex/face types, the Utah teapot control points, and the
//! primitive generators. Ported from geometry.zig / geometry.cpp. Eigen vectors
//! become linalg::Vec3/Vec4; std::vector / ArrayList become Vec.

use crate::linalg::{Vec3, Vec4};
use std::collections::HashMap;

const M_PI: f32 = std::f32::consts::PI;

#[derive(Clone, Copy, Debug)]
pub struct Vertex3D {
    pub position: Vec4,
    pub normal: Vec3,
    pub r: f32,
    pub g: f32,
    pub b: f32,
    pub u: f32,
    pub v: f32,
}

impl Default for Vertex3D {
    fn default() -> Self {
        Self {
            position: Vec4::new(0.0, 0.0, 0.0, 1.0),
            normal: Vec3::zero(),
            r: 1.0,
            g: 1.0,
            b: 1.0,
            u: 0.0,
            v: 0.0,
        }
    }
}

impl Vertex3D {
    #[inline]
    pub fn at(x: f32, y: f32, z: f32) -> Vertex3D {
        Vertex3D { position: Vec4::new(x, y, z, 1.0), ..Default::default() }
    }
}

#[derive(Clone, Copy, Debug)]
pub struct Face {
    pub v0: i32,
    pub v1: i32,
    pub v2: i32,
    pub r: f32,
    pub g: f32,
    pub b: f32,
    pub a: f32,
}

pub type RenderVertexList = Vec<Vertex3D>;
pub type FaceList = Vec<Face>;

#[inline]
fn face(v0: i32, v1: i32, v2: i32, r: f32, g: f32, b: f32, a: f32) -> Face {
    Face { v0, v1, v2, r, g, b, a }
}

